    private void btnAddEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEmployeeActionPerformed
        if(!validateInputs()){
            JOptionPane.showMessageDialog(null,"Please fill all the required data","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        EmployeePojo emp = new EmployeePojo();
        emp.setEmpid(userid);
        emp.setEmpname(txtEmpName.getText());
        emp.setEmpjob(jcJobOptions.getSelectedItem().toString());
        
        try{
            emp.setEmpsal(Double.parseDouble(txtEmpSal.getText()));
            boolean result = EmployeeDAO.addEmployee(emp);
            if(!result){
                JOptionPane.showMessageDialog(null,"Can't add employee! try again!!","Failure!",JOptionPane.ERROR_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null,"Record added successfully !","Success!",JOptionPane.INFORMATION_MESSAGE);
                clearInputFiels();
                loadNextId();
            }
        }
        catch(SQLException sqlex){
                JOptionPane.showMessageDialog(null,"Can't add employee , DB error!","Error",JOptionPane.ERROR_MESSAGE);
        }
        catch(NumberFormatException nfex){
                JOptionPane.showMessageDialog(null,"Please fill digits only in salaty!","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddEmployeeActionPerformed

