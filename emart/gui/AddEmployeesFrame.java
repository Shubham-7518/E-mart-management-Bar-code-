    private void loadNextId() {
        try{
            userid = EmployeeDAO.getNextEmployeeId();
            txtEmpId.setText(userid);
        }
       catch(SQLException sqlex)
       {
           JOptionPane.showMessageDialog(null,"Can't get the Id from database!!","Error!",JOptionPane.ERROR_MESSAGE);
           sqlex.printStackTrace();
       }
        
    }

