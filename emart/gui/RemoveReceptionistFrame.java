    private void loadReceptionistsId() {
        try{
            List<String> recList = ReceptionistDAO.getAllRegisterReceptionistId();
            if(recList.isEmpty()){
                JOptionPane.showMessageDialog(null,"No Receptionist present!","Error",JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            btnRemove.setEnabled(true);
            for(String id:recList){
                jcReceptionistId.addItem(id);
            }
        }
        catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null,"Can't load id's of Receptionist , DB error!","Error",JOptionPane.ERROR_MESSAGE);
        }
    }




