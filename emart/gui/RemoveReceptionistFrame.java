    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
       String userid = jcReceptionistId.getSelectedItem().toString();
       try{
           boolean result = ReceptionistDAO.removeEmployeeById(userid);
           if(!result){
              JOptionPane.showMessageDialog(null,"Can't remove Receptionist!!","Error",JOptionPane.ERROR_MESSAGE);
              return;
           }
           JOptionPane.showMessageDialog(null,"Receptionist removed successfully !!","Success",JOptionPane.INFORMATION_MESSAGE);
           jcReceptionistId.removeItemAt(jcReceptionistId.getSelectedIndex());
           if(jcReceptionistId.getItemCount() == 0){
               JOptionPane.showMessageDialog(null,"No receptionist present!!","Information",JOptionPane.INFORMATION_MESSAGE);
               btnRemove.setEnabled(false);
           }
       }
        catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null,"Can't remove receptionist , DB error!","Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

