    private void loadReceptionistsId() {
        try{
            receptionistMap = ReceptionistDAO.getUnregisteredReceptionist();
            if(receptionistMap.isEmpty()){
                JOptionPane.showMessageDialog(null,"No unregistered Receptionist present!,Information","Error",JOptionPane.INFORMATION_MESSAGE);
                btnAdd.enable(false);
                return;
            }
            
            Set<String> keys = receptionistMap.keySet();
            LinkedHashSet<String> k = new LinkedHashSet(keys);
            Iterator<String> it = k.iterator();
            jcEmployeesId.removeAllItems();
            while(it.hasNext()){
                jcEmployeesId.addItem(it.next());
            }
        }
        catch(SQLException ex){
            
            JOptionPane.showMessageDialog(null,"Can't load id's of Receptionist , DB error!","Error",JOptionPane.ERROR_MESSAGE);
        }
    }
    private int validateInput(){
        if(txtUserId.getText().isEmpty()){
            return 0;
        }
        if(pwdPassword.getPassword().length<4 || rePwdPassword.getPassword().length<4){
            return -1;
        }
        return 1;
    }
    private boolean validatePassword(String a,String b){
        return a.equals(b);
    }
    private void clearInputs(){
        txtEmpName.setText("");
        txtUserId.setText("");
        pwdPassword.setText("");
        rePwdPassword.setText("");
    }
