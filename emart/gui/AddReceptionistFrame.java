    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        int res = validateInput();
        if(res == -1){
            JOptionPane.showMessageDialog(null,"Password must be 4 characters long","Information",JOptionPane.INFORMATION_MESSAGE);
            return;
        }else if(res == 0){
            JOptionPane.showMessageDialog(null,"User id can not be left blank!","Information",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        char[] pwd = pwdPassword.getPassword();
        char[] repwd = rePwdPassword.getPassword();
        String spwd=String.valueOf(pwd);
        String srepwd=String.valueOf(repwd);
        if(!validatePassword(spwd,srepwd)){
            JOptionPane.showMessageDialog(null,"passwords mismatch,should be same!","Information",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try{
            UserPojo usr = new UserPojo();
            usr.setEmpid(jcEmployeesId.getSelectedItem().toString());
            usr.setPassword(spwd);
            usr.setUserid(txtUserId.getText());
            usr.setUsername(txtEmpName.getText());
            usr.setUsertype("Receptionist");
            boolean result = ReceptionistDAO.addReceptionist(usr);
            if(result){
                JOptionPane.showMessageDialog(null,"Record added successfully!!","Successs",JOptionPane.INFORMATION_MESSAGE);
                loadReceptionistsId();
            }
            else{
                JOptionPane.showMessageDialog(null,"Passwords must be same","Error",JOptionPane.ERROR_MESSAGE);
            }
        }
        catch(SQLException sqlex){
            JOptionPane.showMessageDialog(null,"DB error","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

