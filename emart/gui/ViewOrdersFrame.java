    private void generateNextOrderID() {
       try{
           orderID = OrdersDAO.generateNextOrderId();
       }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"OrderID can not be generated","DB Error",JOptionPane.ERROR_MESSAGE);
           return;
       }
    }

    private void populateDataIntoTable(String orderID,String userID) {
        try{
            List<ProductsPojo> productList = OrdersDAO.getOrdersById(orderID, userID);
            Object[] col = new Object[8];
            for(ProductsPojo product:productList){
                col[0] = product.getProductID();
                col[1] = product.getProductName();
                col[2] = product.getProductPrice();
                col[3] = product.getOurPrice();
                col[4] = product.getProductCompany();
                col[5] = product.getQuantity();
                col[6] = product.getTax()+"%";
                col[7] = product.getTotal();
                grandTotal+=product.getTotal();
                model.addRow(col);
            }
            txtGrdTotal.setText(String.valueOf(grandTotal));
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Can not load order from database!","DB error",JOptionPane.ERROR_MESSAGE);
            return;
        }
    }

    private void addOrder(List<ProductsPojo> productList,String orderID) {
       try{
           boolean result = OrdersDAO.addOrders(productList,orderID);
           if(!result){
                JOptionPane.showMessageDialog(null,"Can not add order into database!","Try again",JOptionPane.ERROR_MESSAGE);
                return;
           }else{
               JOptionPane.showMessageDialog(null,"Successfully Added order!","Information",JOptionPane.INFORMATION_MESSAGE);
               return;
           }
       }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Can not add order into database!","Unsuccessful!",JOptionPane.ERROR_MESSAGE);
            return;
       }
    }

    private void loadOrderId() {
       try{
           List<String> orderIdList = OrdersDAO.getOrdersID(UserProfile.getUserid());
           for(String oid:orderIdList){
               jcProductsId.addItem(oid);
           }
       }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"Can not add order id's from database!","Unsuccessful!",JOptionPane.ERROR_MESSAGE);
            return;
       }
    }
