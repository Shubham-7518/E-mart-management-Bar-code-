    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!validateInputs())
            return;
        String selectedUserId = jcReceptionistId.getSelectedItem().toString();
        try{
            boolean result = ReceptionistDAO.updateReceptionist(selectedUserId, password);
            if(!result){
                JOptionPane.showMessageDialog(null,"Can't update password!");
                return;
            }
            JOptionPane.showMessageDialog(null,"Receptonist updated successfully !","Success",JOptionPane.INFORMATION_MESSAGE);
            clearInputs();
        }
         catch(SQLException sqlex){
            JOptionPane.showMessageDialog(null,"Can't update receptionist !","DB Error",JOptionPane.ERROR_MESSAGE);
            sqlex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

