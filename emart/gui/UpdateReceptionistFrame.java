    private void loadReceptionistId() {
      try{
          recList = ReceptionistDAO.getAllRegisteredRecetionist();
          if(recList.isEmpty()){
               JOptionPane.showMessageDialog(null,"No receptionist present !");
          }
          Set<String> keys = recList.keySet();
          for(String key:keys){
              jcReceptionistId.addItem(key);
          }
      }
      catch(SQLException sqlex){
                JOptionPane.showMessageDialog(null,"Can't load the receptonist !","DB Error",JOptionPane.ERROR_MESSAGE);
                sqlex.printStackTrace();
        }
    }

    private boolean validateInputs() {
        char[] pwd = txtPassword.getPassword();
        char[] repwd = txtRePassword.getPassword();
        if(pwd.length < 4 || repwd.length < 4){
             JOptionPane.showMessageDialog(null,"Passwords must be at least 4 charcter long!");
             return false;
        }
        password = String.valueOf(pwd);
        String srepwd = String.valueOf(repwd);
        if(!password.equals(srepwd)){
             JOptionPane.showMessageDialog(null,"Password mismatch !");
             return false;
        }
        return true;
    }

    private void clearInputs() {
        txtPassword.setText("");
        txtRePassword.setText("");
    }

    
    

