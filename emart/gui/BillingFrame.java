    private void txtProductIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtProductIdActionPerformed
        String pid = txtProductId.getText();
        if(pid.isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill productId!","Information!",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try{
            ProductsPojo product = ProductsDAO.getProductById(pid);
            if(product == null){
                JOptionPane.showMessageDialog(null,"Can not load the product!","Error!",JOptionPane.ERROR_MESSAGE);
                return;
            }
            int rowIdx = getRowIndexById(product.getProductID());
            if(rowIdx == -1){
                model = (DefaultTableModel) jtAllProducts.getModel();
                col[0] = product.getProductID();
                col[1] = product.getProductName();
                col[2] = product.getProductPrice();
                col[3] = product.getOurPrice();
                col[4] = product.getProductCompany();
                col[5] = product.getQuantity();
                col[6] = product.getTax()+"%";
                col[7] = product.getTotal();
                grdTotal+=product.getTotal();
                String str = getFormattedString(grdTotal);
                txtGrdTotal.setText(str);
                model.addRow(col);
                productsList.add(product);
            }
            else{
                String quantity = model.getValueAt(rowIdx,5).toString();
                String total = model.getValueAt(rowIdx,7).toString();
                int qu = Integer.parseInt(quantity)+1;
                double to = Double.parseDouble(total)+product.getTotal();
                grdTotal = grdTotal+product.getTotal();
                ProductsPojo p = productsList.get(rowIdx);
                p.setTotal(to);
                p.setQuantity(qu);
                model.setValueAt(qu,rowIdx,5);
                model.setValueAt(to,rowIdx,7);
                String str = getFormattedString(grdTotal);
                txtGrdTotal.setText(str);
            }
        }
         catch(SQLException sqlex){
            JOptionPane.showMessageDialog(null,"Can't load Product!","DB Error",JOptionPane.ERROR_MESSAGE);
            sqlex.printStackTrace();
       }
    }//GEN-LAST:event_txtProductIdActionPerformed

