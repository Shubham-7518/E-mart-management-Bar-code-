    private int getRowIndexById(String pid) {
       for(int i=0; i<productsList.size(); i++){
           ProductsPojo p = productsList.get(i);
           if(p.getProductID().equals(pid))
               return i;
       }
       return -1;
    }
    private String getFormattedString(double grdTotal){
        String str = String.format("%.2f",grdTotal);
        return str;
    }
